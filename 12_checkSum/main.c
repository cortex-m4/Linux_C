unsigned int CheckSum(unsigned char *buf, unsigned int len) //buf为数组，len为数组长度
{ 
    unsigned int i, ret = 0;
 
    for(i=0; i<len; i++)
    {
        ret = (*(buf++)+ret)&0xffff;
	}
    return ret;

}

unsigned int CheckSumNOT(unsigned char *buf, unsigned int len) //buf为数组，len为数组长度
{ 
    unsigned int i, ret = 0;
 
    for(i=0; i<len; i++)
    {
        ret += *(buf++);
    }
     ret = ~ret;
    return ret;

}

unsigned int CheckSumNOT2(unsigned char *buf, unsigned int len) //buf为数组，len为数组长度
{ 
    unsigned int i, ret = 0;
 
    for(i=0; i<len; i++)
    {
        ret = (*(buf++)+ret)&0xffff;
    }
     ret = 0xffff-ret;
    return ret;

}


